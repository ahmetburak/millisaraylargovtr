                            <h3>&nbsp;</h3><br><p>
                                Dolmabahçe Sarayı'nın Batı etkileri altında Avrupa saraylarından örnek alınarak yapılmış bir saray olmasına karşılık, işlevsel kuruluşu ve iç mekân yapısında, "Harem"in -eskisi kadar kesin çizgilerle olmasa da- ayrı bir bölüm olarak kurulmasına özen gösterilmiştir. Ancak Topkapı Sarayı'nın tersine, Harem, artık Saray’dan ayrı tutulmuş bir yapı ya da yapılar topluluğu değildir; aynı çatı altında, aynı yapı bütünlüğü içinde yerleştirilmiş özel bir yaşama birimidir. Dolmabahçe Sarayı, hizmete açıldığı 1856 yılından, halifeliğin kaldırıldığı 1924’e kadar aralıklarla 6 padişaha ve son Osmanlı Halifesi Abdülmecid Efendi’ye ev sahipliği yapmıştır. 1927- 1949 yılları arasında Saray, Cumhurbaşkanlığı makamı olarak kullanılmıştır. Cumhuriyetimizin kurucusu Gazi Mustafa Kemal Atatürk, 1927-1938 yılları arasında İstanbul’daki çalışmalarında Dolmabahçe Sarayı’nı kullanmış ve burada vefat etmiştir. 1926-1984 yılları arasında protokol ve ziyarete kısmen açık olan Saray, 1984 yılından itibaren “müze- saray” olarak geziye açılmıştır.


